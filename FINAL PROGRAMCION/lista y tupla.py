#las listas si se pueden modificar
lista = [1, 2, 3, 4]
print("Lista original:", lista)
#agrega 
lista.append(5)  
print("Lista después de agregar un elemento:", lista)
#elimina
lista.remove(3)  
print("Lista después de eliminar un elemento:", lista)

#las tuplas no se pueden modificar
tupla = (1, 2, 3, 4)
print("Tupla:", tupla)

